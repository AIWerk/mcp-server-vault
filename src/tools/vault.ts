import { z } from 'zod';
import type { VaultClient } from '../api.js';

// ---------------------------------------------------------------------------
// list_vault_items
// ---------------------------------------------------------------------------

export const listVaultItemsInput = {
  filter: z.string().optional()
    .describe('Case-insensitive substring filter on item names. Omit to return all items.'),
  collection: z.enum(['mcp-exposed', 'mcp-agent-created']).optional()
    .describe('Restrict to one collection. Omit to return items from both mcp-exposed and mcp-agent-created.'),
};

export type ListVaultItemsArgs = {
  filter?: string;
  collection?: 'mcp-exposed' | 'mcp-agent-created';
};

export async function listVaultItems(client: VaultClient, args: ListVaultItemsArgs) {
  return client.listItems(args.filter, args.collection);
}

// ---------------------------------------------------------------------------
// get_vault_metadata
// ---------------------------------------------------------------------------

export const getVaultMetadataInput = {
  name: z.string().min(1)
    .describe('Exact item name (case-sensitive) as it appears in the vault.'),
};

